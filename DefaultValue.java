public class DefaultValue{	
	static boolean b;
	static byte c;
	static short d;
	static int e;	
	static long f;
	static float g;
	static double h;
	static char i;
	static String s;

	public static void main(String args[]){
		System.out.println("\nboolean default value = "+b);
		System.out.println("byte default value = "+c);
		System.out.println("short default value = "+d);
		System.out.println("int default value = "+e);
		System.out.println("long default value = "+f);
		System.out.println("float default value = "+g);
		System.out.println("double default value = "+h);
		System.out.println("char default value = "+i);
		System.out.println("String default value= "+s);
		
	}
}